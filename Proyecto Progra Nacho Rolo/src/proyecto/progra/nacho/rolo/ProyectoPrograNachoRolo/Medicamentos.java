package proyecto.progra.nacho.rolo.ProyectoPrograNachoRolo;


public class Medicamentos {
    private String Nombre;
    private String Funcion;
    private String Inventario;
    private String ConsumoPacientes;
    private String DependenciaPacientes;

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public void setFuncion(String Funcion) {
        this.Funcion = Funcion;
    }

    public void setInventario(String Inventario) {
        this.Inventario = Inventario;
    }

    public void setConsumoPacientes(String ConsumoPacientes) {
        this.ConsumoPacientes = ConsumoPacientes;
    }

    public void setDependenciaPacientes(String DependenciaPacientes) {
        this.DependenciaPacientes = DependenciaPacientes;
    }

    public String getNombre() {
        return Nombre;
    }

    public String getFuncion() {
        return Funcion;
    }

    public String getInventario() {
        return Inventario;
    }

    public String getConsumoPacientes() {
        return ConsumoPacientes;
    }

    public String getDependenciaPacientes() {
        return DependenciaPacientes;
    }
    
   
}
